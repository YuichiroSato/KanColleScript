package Script;

public class Damage {

	int taiha;
	int tyuha;
	int syoha;
	
	public Damage(int tai, int tyu, int syo) {
		taiha = tai;
		tyuha = tyu;
		syoha = syo;
	}
	
	public int getTaiha() {
		return taiha;
	}
	
	public int getTyuha() {
		return tyuha;
	}

	public int getSyoha() {
		return syoha;
	}

}
